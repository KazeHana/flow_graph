CREATE table flowgraph_t_version(	
  ID NUMBER NOT NULL PRIMARY KEY,
  GRAPH NUMBER,
  CODE NUMBER,
  DESCRIPTION VARCHAR(1000),
  CREATOR VARCHAR(50),
  CREATE_AT DATE,
  PARENT NUMBER,
  DMO CLOB,
  LAST_UPDATE DATE
);

CREATE SEQUENCE flowgraph_s_version 
MINVALUE 1 
MAXVALUE 9999999999999999999999999999 
INCREMENT BY 1 
START WITH 1 
CACHE 20 
NOORDER 
NOCYCLE 
;

